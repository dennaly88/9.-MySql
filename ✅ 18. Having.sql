/----------------------------------------------------------/
     HAVING 
/---------------------------------------------------------/

/*
    El comando HAVING se utiliza para filtrar los resultados de una consulta después de aplicar una función de agregación
*/

SELECT producto, SUM(monto) AS total_ventas
FROM ventas
GROUP BY producto
HAVING total_ventas > 1000; 

SELECT carrera, COUNT(id_estudiante) AS total_estudiantes
FROM estudiantes    
GROUP BY carrera
HAVING total_estudiantes > 50;

SELECT region, estado_pedido, COUNT(*) AS numero_pedidos
FROM pedidos
GROUP BY region, estado_pedido
HAVING numero_pedidos > 10;



